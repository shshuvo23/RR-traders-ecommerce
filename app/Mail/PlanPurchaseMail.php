<?php

namespace App\Mail;

use Illuminate\Bus\Queueable;
use Illuminate\Contracts\Queue\ShouldQueue;
use Illuminate\Mail\Mailable;
use Illuminate\Queue\SerializesModels;

class PlanPurchaseMail extends Mailable
{
    use Queueable, SerializesModels;

    public $data;
    /**
     * Create a new message instance.
     *
     * @return void
     */
    public function __construct($data)
    {
        $this->data = $data;
    }

    /**
     * Build the message.
     *
     * @return $this
     */
    public function build()
    {
        return $this->subject("Confirmation: Your Subscription Purchase Complete! " . config('app.name'))
        ->markdown('mail.plan-purchase-mail')->from(getSetting()->email)
        ->with('mailData', $this->data);
        // return $this->markdown('mail.plan-purchase-mail');
    }
}
